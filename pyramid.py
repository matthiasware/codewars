def pyramid(n):
    if n == 0:
        return "\n"
    r = ""
    i = n-1
    j = 0
    while i > 0:
        r += i * " " + "/" + j * " " +  "\\" + "\n"
        i-=1
        j+=2
    r += "/" + j * "_" + "\\" + "\n"
    return r

print(pyramid(34))
